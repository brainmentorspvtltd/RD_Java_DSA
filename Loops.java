package codes;

public class Loops {

	public static void main(String[] args) {
		for(int i = 1; i<=10; i++) {
		System.out.println("Amit "+i); // 10 times print
		}
		
		//while(1); // error
		int j = 1; // initalize
		while(j<=10) { // while(true)boolean 
			System.out.println("J is "+j);
			j++;
		}
	}

}
